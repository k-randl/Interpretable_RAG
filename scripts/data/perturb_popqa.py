def _ret_token_processor(s: str) -> str:
    return s.replace('▁', ' ')

def _gen_token_processor(s: str) -> str:
    return s.replace('Ġ', ' ').strip('Ċ')


_stemmer = PorterStemmer()

def _normalize_text(text: str) -> list[str]:
    return [_stemmer.stem(w) for w in re.findall(r'[^\s\.,;:!?\(\)\[\]{}_\-\+\*]+', text.lower())]

def _supports(prediction: str, ground_truths: list[str]) -> float:
    pred_tokens = _normalize_text(prediction)
    for gt in ground_truths:
        gt_tokens = _normalize_text(gt)

        n = len(gt_tokens)
        if n == 0: continue

        for i in range(0, len(pred_tokens)-n+1):
            if pred_tokens[i:i+n] == gt_tokens:
                return True
    return False


