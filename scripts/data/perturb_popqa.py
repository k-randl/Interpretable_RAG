# Perturbation conditions generated by PromptPerturbationModule:
#   Setup A (Top-k vs Low-k):          A1=[top,low],    A2=[low,top],    A3=shuffled
#   Setup B (Top-k vs Random-corpus):  B1=[top,random], B2=[random,top], B3=shuffled
#   Setup C (Top-k vs Adjacent-k):     C1=[top,adj],    C2=[adj,top],    C3=shuffled
CONDITION_KEYS = ['B1', 'B2', 'B3']

#================================================================================#
# Main function:                                                                 #
#================================================================================#
def main(generator: str, retriever: str, k: int = 5, devices: str | None = None):
    results_dir = f'../../results/{RET_FOLDERS[retriever]}/{GEN_FOLDERS[generator]}'
    warg_path = f'{results_dir}/warg_popqa.csv'
    corpus_path = f'../../data/popqa/{RET_FOLDERS[retriever]}/documents.json'
    perturb_path = f'{results_dir}/perturbations_popqa.csv'
    perturbations_dir = f'{results_dir}/perturbations_popqa/'

    os.makedirs(perturbations_dir, exist_ok=True)

    if devices is not None:
        os.environ["CUDA_VISIBLE_DEVICES"] = devices

    with open(corpus_path) as f:
        corpus = json.load(f)

    model = ExplainableAutoModelForRAG(**RET_INIT_ARGS[retriever], **GEN_INIT_ARGS[generator])
    perturbations = PromptPerturbationModule(seed=42)

    warg = pd.read_csv(warg_path, index_col=None)

    gen_cols = {c: f'generation_{c}' for c in CONDITION_KEYS}
    if os.path.exists(perturb_path):
        results = pd.read_csv(perturb_path, index_col=None)
    else:
        results = warg[['query_id', 'query', 'possible_answers', 'passages']].copy()
        for col in gen_cols.values():
            results[col] = None

    for i, (qry, ctx_raw) in enumerate(results[['query', 'passages']].values):
        ranked_docs = eval(ctx_raw)

        conditions = {}
        if any(k.startswith('A') for k in CONDITION_KEYS):
            conditions.update(perturbations.generate_setup_a(ranked_docs, k=k))
        if any(k.startswith('B') for k in CONDITION_KEYS):
            conditions.update(perturbations.generate_setup_b(ranked_docs[:k], corpus, k=k))
        if any(k.startswith('C') for k in CONDITION_KEYS):
            conditions.update(perturbations.generate_intertwined_setup(ranked_docs, k=k))

        ret_kwargs = RET_CALL_ARGS[retriever].get('retriever_kwargs', {})
        gen_kwargs = GEN_CALL_ARGS[generator]['generator_kwargs']

        for cond_key in CONDITION_KEYS:
            cond_docs = conditions[cond_key]
            col = gen_cols[cond_key]
            if not pd.isnull(results.loc[i, col]):
                print(f'Skipping query {i:d} condition {cond_key}.')
                continue

            # Encode contexts with the retriever keeping cond_docs order in the
            # internal state (reorder=False).  The returned document list is
            # discarded; what matters is that self._x['context'] is ordered to
            # match the order we pass to the generator below.
            model.retriever(
                model.retriever_query_format.format(query=qry),
                contexts=cond_docs,
                k=len(cond_docs),
                reorder=False,
                output_texts=False,
                compute_grad=False,
                output_attentions=False,
                output_hidden_states=False,
                **ret_kwargs,
            )

            # Generate with cond_docs in their exact perturbed order.
            results.loc[i, col] = model.generator.explain_generate(
                qry, cond_docs, **gen_kwargs,
            )[-1]['content']

            model.save_values(
                perturbations_dir, f'{cond_key.lower()}_{i:d}.pkl',
                ret_methods=['intGrad'], gen_aggregations=['token'], batch_size=16,
            )
        

        results.to_csv(perturb_path, index=False)

    for cond_key, col in gen_cols.items():
        supp_col = f'supp_{cond_key}'
        results[supp_col] = results.apply(
            lambda row: _supports(
                str(row[f'generation_{cond_key}']),
                ast.literal_eval(row['possible_answers']),
            ) if pd.notna(row[f'generation_{cond_key}']) else float('nan'),
            axis=1,
        )
    results.to_csv(perturb_path, index=False)

